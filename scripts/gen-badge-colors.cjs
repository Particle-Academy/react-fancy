const fs = require("fs");
const COLORS = ["slate","gray","zinc","neutral","stone","red","orange","amber","yellow","lime","green","emerald","teal","cyan","sky","blue","indigo","violet","purple","fuchsia","pink","rose"];
// Bright hues need dark text on a solid fill for legible contrast.
const LIGHT = new Set(["amber","yellow","lime"]);

const solid = (c) => LIGHT.has(c)
  ? `bg-${c}-400 text-${c}-950 dark:bg-${c}-300 dark:text-${c}-950`
  : `bg-${c}-600 text-white dark:bg-${c}-500`;
const outline = (c) => `border border-${c}-300 text-${c}-700 dark:border-${c}-600 dark:text-${c}-300`;
const soft = (c) => `bg-${c}-100 text-${c}-700 dark:bg-${c}-900/30 dark:text-${c}-300`;
const dot = (c) => `bg-${c}-500`;

const rec = (fn) => "{\n" + COLORS.map((c) => `  ${c}: "${fn(c)}",`).join("\n") + "\n}";

const out = `// AUTO-GENERATED by scripts/gen-badge-colors — do not edit by hand.
// Literal Tailwind class strings for every color in the v4 palette so the
// scanner can detect them in the published dist (template-built names are
// invisible to Tailwind's JIT). Regenerate after changing the palette.
import type { Color } from "../../utils/types";

export const badgeSolid: Record<Color, string> = ${rec(solid)};

export const badgeOutline: Record<Color, string> = ${rec(outline)};

export const badgeSoft: Record<Color, string> = ${rec(soft)};

export const badgeDot: Record<Color, string> = ${rec(dot)};
`;
fs.writeFileSync(__dirname + "/../src/components/Badge/Badge.colors.ts", out);
console.log("wrote Badge.colors.ts (" + COLORS.length + " colors × 4 maps)");
