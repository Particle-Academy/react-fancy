const fs = require("fs");
const COLORS = ["slate","gray","zinc","neutral","stone","red","orange","amber","yellow","lime","green","emerald","teal","cyan","sky","blue","indigo","violet","purple","fuchsia","pink","rose"];

// Bar/circular fill and the value label. The fill (bg / stroke) sits on a
// neutral track so the -500/-400 shade reads well for every hue; the text
// label uses -600/-400. Light hues need no special-casing here because the
// fill is a solid bar, not text on a colored surface.
const fill = (c) => `bg-${c}-500 dark:bg-${c}-400`;
const stroke = (c) => `stroke-${c}-500 dark:stroke-${c}-400`;
const text = (c) => `text-${c}-600 dark:text-${c}-400`;

const rec = (fn) => "{\n" + COLORS.map((c) => `  ${c}: "${fn(c)}",`).join("\n") + "\n}";

const out = `// AUTO-GENERATED by scripts/gen-progress-colors — do not edit by hand.
// Literal Tailwind class strings for every color in the v4 palette so the
// scanner can detect them in the published dist (template-built names are
// invisible to Tailwind's JIT). Regenerate after changing the palette.
import type { Color } from "../../utils/types";

export const progressFill: Record<Color, string> = ${rec(fill)};

export const progressStroke: Record<Color, string> = ${rec(stroke)};

export const progressText: Record<Color, string> = ${rec(text)};
`;
fs.writeFileSync(__dirname + "/../src/components/Progress/Progress.colors.ts", out);
console.log("wrote Progress.colors.ts (" + COLORS.length + " colors × 3 maps)");
