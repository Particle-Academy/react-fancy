const fs = require("fs");
const COLORS = ["slate","gray","zinc","neutral","stone","red","orange","amber","yellow","lime","green","emerald","teal","cyan","sky","blue","indigo","violet","purple","fuchsia","pink","rose"];

// The 'on'/checked track is a solid fill only (no text on it), so the
// plain bg-{c}-500 pattern reads fine across the whole palette — even the
// bright hues (amber/yellow/lime) need no contrast adjustment here.
const track = (c) => `bg-${c}-500`;

const rec = (fn) => "{\n" + COLORS.map((c) => `  ${c}: "${fn(c)}",`).join("\n") + "\n}";

const out = `// AUTO-GENERATED by scripts/gen-switch-colors — do not edit by hand.
// Literal Tailwind class strings for every color in the v4 palette so the
// scanner can detect them in the published dist (template-built names are
// invisible to Tailwind's JIT). Regenerate after changing the palette.
import type { Color } from "../../../utils/types";

export const switchTrack: Record<Color, string> = ${rec(track)};
`;
fs.writeFileSync(__dirname + "/../src/components/inputs/Switch/Switch.colors.ts", out);
console.log("wrote Switch.colors.ts (" + COLORS.length + " colors)");
