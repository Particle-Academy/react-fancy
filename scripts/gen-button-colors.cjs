const fs = require("fs");
const COLORS = ["slate","gray","zinc","neutral","stone","red","orange","amber","yellow","lime","green","emerald","teal","cyan","sky","blue","indigo","violet","purple","fuchsia","pink","rose"];
// Bright hues need dark text on a solid fill for legible contrast.
const LIGHT = new Set(["amber","yellow","lime"]);

const solid = (c) => LIGHT.has(c)
  ? `bg-${c}-400 text-${c}-950 border border-${c}-500 hover:bg-${c}-500 dark:bg-${c}-300 dark:text-${c}-950 dark:border-${c}-400 dark:hover:bg-${c}-400`
  : `bg-${c}-500 text-white border border-${c}-600 hover:bg-${c}-600 dark:bg-${c}-600 dark:border-${c}-500 dark:hover:bg-${c}-500`;
const ghost = (c) => `bg-transparent text-${c}-600 hover:bg-${c}-50 dark:text-${c}-400 dark:hover:bg-${c}-950/50`;

const rec = (fn) => "{\n" + COLORS.map((c) => `  ${c}: "${fn(c)}",`).join("\n") + "\n}";

const out = `// AUTO-GENERATED by scripts/gen-button-colors — do not edit by hand.
// Literal Tailwind class strings for every color in the v4 palette so the
// scanner can detect them in the published dist (template-built names are
// invisible to Tailwind's JIT). Regenerate after changing the palette.
import type { Color } from "../../utils/types";

export const buttonColorClasses: Record<Color, string> = ${rec(solid)};

export const buttonGhostClasses: Record<Color, string> = ${rec(ghost)};
`;
fs.writeFileSync(__dirname + "/../src/components/Button/Button.colors.ts", out);
console.log("wrote Button.colors.ts (" + COLORS.length + " colors × 2 maps)");
