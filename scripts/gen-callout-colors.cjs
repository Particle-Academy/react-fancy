const fs = require("fs");
const COLORS = ["slate","gray","zinc","neutral","stone","red","orange","amber","yellow","lime","green","emerald","teal","cyan","sky","blue","indigo","violet","purple","fuchsia","pink","rose"];

// Soft tinted surface: light bg-50 + dark text-900 already gives legible
// contrast for every hue (including light ones), so no per-hue special-casing
// is needed here — the same recipe applies to all 22 colors.
const container = (c) =>
  `border-${c}-500 bg-${c}-50 text-${c}-900 dark:border-${c}-400 dark:bg-${c}-950/50 dark:text-${c}-100`;
const icon = (c) => `text-${c}-500 dark:text-${c}-400`;

const rec = (fn) => "{\n" + COLORS.map((c) => `  ${c}: "${fn(c)}",`).join("\n") + "\n}";

const out = `// AUTO-GENERATED by scripts/gen-callout-colors — do not edit by hand.
// Literal Tailwind class strings for every color in the v4 palette so the
// scanner can detect them in the published dist (template-built names are
// invisible to Tailwind's JIT). Regenerate after changing the palette.
import type { Color } from "../../utils/types";

export const calloutContainer: Record<Color, string> = ${rec(container)};

export const calloutIcon: Record<Color, string> = ${rec(icon)};
`;
fs.writeFileSync(__dirname + "/../src/components/Callout/Callout.colors.ts", out);
console.log("wrote Callout.colors.ts (" + COLORS.length + " colors × 2 maps)");
